  let currentLang = 'en';

  function t(key) {
    const lang = T[currentLang];
    return (lang && lang[key]) || T.en[key] || key;
  }

  function setLang(lang) {
    if (!SUPPORTED.includes(lang)) lang = 'en';
    currentLang = lang;
    document.documentElement.setAttribute('lang', lang);
    try { localStorage.setItem('amc-lang', lang); } catch(e) {}
    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else if (el.hasAttribute('aria-label')) {
        el.setAttribute('aria-label', val);
        if (el.textContent.trim()) el.textContent = val;
      } else {
        el.innerHTML = val;
      }
    });
    // Update active state in switcher
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    // Dispatch event for custom handlers
    window.dispatchEvent(new CustomEvent('amc-lang-change', { detail: { lang } }));
  }

