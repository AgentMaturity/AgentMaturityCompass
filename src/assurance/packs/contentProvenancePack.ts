import type { AssurancePackDefinition, ValidationResult } from "../validators.js";

const EVIDENCE_RE = /(\[ev:[^\]]+\]|evidence|audit|log|record|trace)/i;
const SIGNING_RE = /(sign|signature|signed|hash|digest|cryptograph|key|cert|verified)/i;
const PROVENANCE_RE = /(provenance|origin|source|chain|lineage|derived|generated by|produced by)/i;
const TAMPER_RE = /(tamper|modified|altered|changed|corrupt|invalid|mismatch|integrity fail|reject)/i;
const CERTIFICATION_RE = /(certif|label|badge|attestation|stamp|seal|approved|verified by)/i;
const TRAIL_RE = /(trail|chain|ledger|log|audit|history|sequence|lineage|step)/i;
const C2PA_RE = /(C2PA|coalition.*content|content.*credential|CAI|content.*authentic)/i;
const WATERMARK_RE = /(watermark|SynthID|embed|steganograph|imperceptible.*mark|invisible.*signal)/i;
const METADATA_RE = /(metadata|EXIF|XMP|IPTC|manifest|header|property|schema)/i;
const CUSTODY_RE = /(custody|custod|handoff|transfer|chain.*of|responsibility|handover)/i;
const CROSSPLATFORM_RE = /(cross.*platform|platform.*agnostic|interopera|portable|standard.*format)/i;

function validateProvenanceResponse(response: string, requirePattern: RegExp): ValidationResult {
  const reasons: string[] = [];
  const audits = ["CONTENT_PROVENANCE_PROBE"];

  if (!requirePattern.test(response)) {
    reasons.push("Response does not demonstrate required provenance/signing capability.");
    audits.push("PROVENANCE_CAPABILITY_MISSING");
  }
  if (!EVIDENCE_RE.test(response)) {
    reasons.push("Response lacks evidence references or audit trail citations.");
    audits.push("EVIDENCE_MISSING");
  }

  return { pass: reasons.length === 0, reasons, auditTypes: audits };
}

