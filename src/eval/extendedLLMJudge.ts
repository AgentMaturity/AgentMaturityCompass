// ---------------------------------------------------------------------------
// Extended LLM Judge Engine
// ---------------------------------------------------------------------------

export class ExtendedLLMJudgeEngine extends LLMJudgeEngine {
  
  /**
   * Evaluate extended metrics
   */
  async evaluateExtended(
    metrics: (keyof typeof EXTENDED_JUDGE_PROMPTS)[],
    context: JudgeContext
  ): Promise<JudgeResult[]> {
    const results: JudgeResult[] = [];
    
    for (const metric of metrics) {
      const result = await this.evaluateExtendedMetric(metric, context);
      results.push(result);
    }

    return results;
  }

  /**
   * Evaluate a single extended metric
   */
  async evaluateExtendedMetric(
    metric: keyof typeof EXTENDED_JUDGE_PROMPTS,
    context: JudgeContext
  ): Promise<JudgeResult> {
    const prompt = this.buildExtendedPrompt(metric, context);
    const response = await this.callJudgeModel(prompt);
    return this.parseJudgeResponse(metric, response);
  }

  /**
   * Get all content quality metrics
   */
  async evaluateContentQuality(context: JudgeContext): Promise<JudgeResult[]> {
    const contentMetrics: (keyof typeof EXTENDED_JUDGE_PROMPTS)[] = [
      'fluency',
      'summarizationQuality',
      'groundedness',
      'outputIntegrity'
    ];
    return this.evaluateExtended(contentMetrics, context);
  }

  /**
   * Get all safety and privacy metrics
   */
  async evaluatePrivacySafety(context: JudgeContext): Promise<JudgeResult[]> {
    const privacyMetrics: (keyof typeof EXTENDED_JUDGE_PROMPTS)[] = [
      'piiLeakage'
    ];
    return this.evaluateExtended(privacyMetrics, context);
  }

  /**
   * Get all conversational metrics
   */
  async evaluateConversational(context: JudgeContext): Promise<JudgeResult[]> {
    const conversationalMetrics: (keyof typeof EXTENDED_JUDGE_PROMPTS)[] = [
      'multiTurnConsistency',
      'contextualRecall'
    ];
    return this.evaluateExtended(conversationalMetrics, context);
  }

  /**
   * Get all task-specific metrics
   */
  async evaluateTaskSpecific(context: JudgeContext): Promise<JudgeResult[]> {
    const taskMetrics: (keyof typeof EXTENDED_JUDGE_PROMPTS)[] = [
      'toolUseQuality'
    ];
    return this.evaluateExtended(taskMetrics, context);
  }

  /**
   * Comprehensive evaluation with all 17 metrics
   */
  async evaluateComprehensive(context: JudgeContext): Promise<JudgeResult[]> {
    // Get base metrics from parent class
    const baseResults = await this.evaluateAll(context);
    
    // Get all extended metrics
    const extendedMetrics = Object.keys(EXTENDED_JUDGE_PROMPTS) as (keyof typeof EXTENDED_JUDGE_PROMPTS)[];
    const extendedResults = await this.evaluateExtended(extendedMetrics, context);
    
    return [...baseResults, ...extendedResults];
  }

  private buildExtendedPrompt(metric: keyof typeof EXTENDED_JUDGE_PROMPTS, context: JudgeContext): string {
    let prompt = EXTENDED_JUDGE_PROMPTS[metric];
    
    // Replace placeholders
    prompt = prompt.replace(/{input}/g, context.input || '');
    prompt = prompt.replace(/{output}/g, context.output || '');
    prompt = prompt.replace(/{context}/g, context.context || '');
    prompt = prompt.replace(/{expected}/g, context.expected || '');
    
    return prompt;
  }

  /**
   * Get metric categories for organized evaluation
   */
  getMetricCategories(): Record<string, string[]> {
    return {
      rag: ['answerRelevancy', 'faithfulness', 'contextualPrecision', 'contextualRecall', 'groundedness'],
      safety: ['toxicity', 'biasDetection', 'piiLeakage'],
      quality: ['coherence', 'fluency', 'summarizationQuality', 'outputIntegrity'],
      alignment: ['promptAlignment', 'jsonCorrectness'],
      conversation: ['multiTurnConsistency'],
      task: ['toolUseQuality'],
      detection: ['hallucinationDetection']
    };
  }

  /**
   * Evaluate by category
   */
  async evaluateByCategory(category: string, context: JudgeContext): Promise<JudgeResult[]> {
    const categories = this.getMetricCategories();
    const metrics = categories[category];
    
    if (!metrics) {
      throw new Error(`Unknown category: ${category}`);
    }

    const results: JudgeResult[] = [];
    
    for (const metric of metrics) {
      if (metric in EXTENDED_JUDGE_PROMPTS) {
        const result = await this.evaluateExtendedMetric(
          metric as keyof typeof EXTENDED_JUDGE_PROMPTS, 
          context
        );
        results.push(result);
      } else {
        // Base metric from parent class
        const result = await this.evaluateMetric(metric as any, context);
        results.push(result);
      }
    }

    return results;
  }
}

// ---------------------------------------------------------------------------
// Metric Aggregation Utilities
// ---------------------------------------------------------------------------

export interface MetricSummary {
  category: string;
  averageScore: number;
  minScore: number;
  maxScore: number;
  metricCount: number;
  results: JudgeResult[];
}

export function aggregateMetricsByCategory(results: JudgeResult[]): MetricSummary[] {
  const engine = new ExtendedLLMJudgeEngine();
  const categories = engine.getMetricCategories();
  const summaries: MetricSummary[] = [];

  for (const [category, metricNames] of Object.entries(categories)) {
    const categoryResults = results.filter(r => metricNames.includes(r.metric));
    
    if (categoryResults.length === 0) continue;

    const scores = categoryResults.map(r => r.score);
    const summary: MetricSummary = {
      category,
      averageScore: scores.reduce((a, b) => a + b, 0) / scores.length,
      minScore: Math.min(...scores),
      maxScore: Math.max(...scores),
      metricCount: categoryResults.length,
      results: categoryResults
    };

    summaries.push(summary);
  }

  return summaries;
}

export function calculateOverallScore(results: JudgeResult[]): number {
  if (results.length === 0) return 0;
  return results.reduce((sum, result) => sum + result.score, 0) / results.length;
}